class StockItem {
  String id;
  String nama;
  String kategori;
  String jenis;
  String kode;
  String satuan;
  int jumlah;
  double harga;

  StockItem({
    required this.id,
    required this.nama,
    required this.kategori,
    required this.jenis,
    required this.kode,
    required this.satuan,
    required this.jumlah,
    required this.harga,
  });
}
